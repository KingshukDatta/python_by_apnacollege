# Tuple

tup = (2,1,3,1)
print(type(tup))
print(tup)
print(tup[1])
print("\n")
# Tuple Slicing

print(tup[1:3]) # Just like list where last index mentioned is excluded

# Tuple Methods

print(tup.index(1)) # Returns index number where the element is first found
print(tup.count(1)) # Returns the total number of mentioned elements in Tuple
 