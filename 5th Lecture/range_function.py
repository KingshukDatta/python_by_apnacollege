# Printing even numbers using range function.
print("Even numbers: ")
for i in range(2,100,2): # starts from "2" ends at "100" and steps 2 means after 2 it skips 3 and prints "4".
    print(i)

# printing odd numbers using range function.
print("Odd numbers: ")
for j in range(1,100,2):
    print(j)